package tech.intellispaces.jaquarius.annotationprocessor;

/**
 * The artifact types.
 */
public enum ArtifactTypes {

  /**
   * The movable pure object interface,
   */
  MovablePureObject,

  /**
   * The unmovable pure object interface.
   */
  UnmovablePureObject,

  /**
   * The undefined pure object interface.
   */
  UndefinedPureObject,

  /**
   * The movable object handle interface.
   */
  MovableObjectHandle,

  /**
   * The unmovable object handle interface.
   */
  UnmovableObjectHandle,

  /**
   * The undefined object handle interface.
   */
  UndefinedObjectHandle,

  /**
   * The channel interface.
   */
  Channel,

  /**
   * The mapper guide interface.
   */
  Mapper,

  /**
   * The mover guide interface.
   */
  Mover,

  /**
   * The mapper of moving guide interface.
   */
  MapperOfMoving
}
